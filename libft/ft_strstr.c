#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	size_t	i;

	i = 0;
	if (needle[0] == '\0')
		return ((char*)haystack);
	while (haystack[i] != '\0')
	{
		if (haystack[i] == needle[0] && ft_strncmp((char*)(haystack + i),
					needle, ft_strlen((char*)needle)) == 0)
			return ((char*)(haystack + i));
		i++;
	}
	return (NULL);
}
