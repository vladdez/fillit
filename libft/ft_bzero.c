#include "libft.h"

void	*ft_bzero(void *b, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = (char*)b;
	while (i < n)
	{
		tmp[i] = '\0';
		i++;
	}
	return (b);
}
