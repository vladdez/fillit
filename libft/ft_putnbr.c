#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	if (n < 0)
		ft_putchar('-');
	if (ft_abs(n) > 9 || n == -2147483648)
		ft_putnbr(ft_abs(n / 10));
	c = '0' + ft_abs((n % 10));
	ft_putchar(c);
}
