int		ft_strequ(char const *s1, char const *s2)
{
	int		n;

	n = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[n] != '\0' || s2[n] != '\0')
	{
		if (s1[n] == s2[n])
			n++;
		else
			return (0);
	}
	return (1);
}
