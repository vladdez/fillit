#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t size;
	size_t i;

	i = 0;
	size = ft_strlen(dest);
	while (i < n && src[i] != '\0')
	{
		dest[size + i] = src[i];
		i++;
	}
	dest[size + i] = '\0';
	return (dest);
}
